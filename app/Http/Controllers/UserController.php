     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $users = User::get();
        
        if(Auth::user()->role == 'student'){
            return view('Student.members',  [
                'users'=>$users,
            ]); 
        } 
        elseif (Auth::user()->role == 'teacher'){
            return view('teacher-members', [
                'users'=>$users,
        ]) ;
        }
    }

